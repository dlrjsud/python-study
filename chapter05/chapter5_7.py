# 리스트 집계 와 정렬

sale = [80, 60, 22, 50, 75]
print('현재 데이터는', sale, '입니다.')

print('최대 데이터는', max(sale), '입니다.')
print('최소 데이터는', min(sale), '입니다.')  # max min sum 리스트의 집계

print('데이터의 합계는', sum(sale), '입니다.')

print('정렬된 데이터는', sorted(sale), '입니다.')  # sorted 리스트의 정렬

print('역순된 데이터는', sorted(sale, reverse=True), '입니다.') #내림차순 정렬
