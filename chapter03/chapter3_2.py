# 식의 값 출력
print('1+2는', 1 + 2, '입니다')

# 변수 연산
price = 50
num = 10
total = price * num  #price와 num 값을 *한것을 total에 대입
print('단가는', price, '원입니다.')
print('판매개수는', num, '개입니다.')
print('합계 급액은', total, '원입니다.')

total = total - 100   #total에서 100을 -것을 total에 대입

print('할인하면', total, '원입니다.')
